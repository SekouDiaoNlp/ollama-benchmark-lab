"""
Local LLM execution client for Ollama.

This module provides the LLMClient class, which securely wraps local inference
via the Ollama CLI without relying on any external or remote APIs.

Example:
    >>> client = LLMClient()
    >>> patch = client.generate_patch("Fix this issue: TypeError...")
"""

import subprocess
from typing import Any, Dict, Optional

# Assuming Console is correctly typed
from benchmark.utils.console import Console


class LLMClient:
    """
    LOCAL-ONLY Ollama client.

    IMPORTANT:
    - No OpenAI / remote APIs allowed
    - Only local ollama models
    - Optimized for fastest iteration model

    Attributes:
        console (Console): The terminal UI output logger.
        model (str): The local Ollama model to use for inference.
    """

    def __init__(self) -> None:
        """
        Initialize the LLMClient.
        """
        self.console: Console = Console()

        # FASTEST MODEL (based on benchmark results provided)
        self.model: str = "codegemma:2b-acting-noyap"

    def generate_patch(self, prompt: str) -> str:
        """
        Generate a unified diff patch utilizing a local Ollama model.

        Args:
            prompt (str): The prompt text explaining the issue and requesting a patch.

        Returns:
            str: The raw patch generated by the LLM.

        Raises:
            RuntimeError: If the Ollama subprocess fails or exits with an error.
            FileNotFoundError: If the 'ollama' executable is missing from the system PATH.
        """
        self.console.step(f"Using local model: {self.model}")

        try:
            proc = subprocess.run(
                ["ollama", "run", self.model],
                input=prompt,
                text=True,
                capture_output=True,
            )

            if proc.returncode != 0:
                self.console.error(proc.stderr)
                raise RuntimeError(f"Ollama generation failed: {proc.stderr}")

            output: str = proc.stdout.strip()

            if not output:
                self.console.warn("Empty response from model")

            return output

        except FileNotFoundError:
            self.console.error("Ollama is not installed or not in PATH")
            raise